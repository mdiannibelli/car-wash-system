package com.example.car_wash.data.model;

public class User {
    private String uid;
    private String name;
    private String email;
    private String role;
    private String phone;

    public User() {}

    public User(String uid, String name, String email, String role, String phone) {
        this.uid = uid;
        this.name = name;
        this.email = email;
        this.role = role;
        this.phone = phone;
    }

    public String getUid() {
        return uid;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getRole() {
        return role;
    }

    public String getPhone() {
        return phone;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
